public class FileUtils {

	@Keyword
	public String generatePath(String directoryPath, String filePath) {
		// Creating a File object using the provided file path
		File file = new File(filePath);

		// Extracting the file name from the given file path
		String fileName = file.getName();

		 String newFilePath = directoryPath + File.separator + fileName;

        // Printing the new concatenated path
        println(newFilePath);

        // Returning the concatenated file path
        return newFilePath;
	}
